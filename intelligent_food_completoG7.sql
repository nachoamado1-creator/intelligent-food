-- =====================================================================
-- INTELLIGENT FOOD - Base de Datos Relacional
-- Trabajo Práctico Especial - Grupo 7
-- Análisis y Diseño de Sistemas II
-- Basado en el Diagrama Entidad-Relación (DER) del proyecto
-- Motor: MySQL 8.x / MariaDB 10.x (InnoDB, utf8mb4)
-- =====================================================================
-- Convenciones tomadas del DER:
--  - Producto <--Tiene--> Detalle_de_compra (1:N)
--  - Producto <--Produce--> Cocina_Central (N:M, vía orden de producción)
--  - Producto <--Stock--> Heladera (N:M, entidad asociativa "Stock")
--  - Heladera <--Asociada--> Compras (1:N, una compra de una heladera)
--  - Heladera <--Gestiona--> Administracion_de_heladeras (1:N)
--  - Heladera <--Actualizaciones_de_mantenimiento--> Encargado_mantenimiento (N:M)
--  - Usuario <--Realiza--> Compras (comprador, 1:N)
--  - Usuario <--Retira--> Compras (consumidor final, 1:N)
--  - Usuario <--Tiene--> Cupones (1:N)
--  - Usuario <--Tiene--> Grupo (N:M, vía tabla Grupo)
--  - Grupo <--Utiliza--> Saldo (1:N)
--  - Compras <--Tiene--> Detalle_de_compra (1:N)
--  - Detalle_de_compra / Producto <--Comentarios--> Usuario
-- =====================================================================

DROP DATABASE IF EXISTS intelligent_food;
CREATE DATABASE intelligent_food
    CHARACTER SET utf8mb4
    COLLATE utf8mb4_unicode_ci;
USE intelligent_food;

SET FOREIGN_KEY_CHECKS = 0;

-- =====================================================================
-- 1. USUARIO
-- =====================================================================
CREATE TABLE usuario (
    id_usuario          INT AUTO_INCREMENT PRIMARY KEY,
    nombre              VARCHAR(60)  NOT NULL,
    apellido            VARCHAR(60)  NOT NULL,
    dni                 VARCHAR(15)  NOT NULL,
    tipo_doc            VARCHAR(10)  NOT NULL DEFAULT 'DNI',
    sexo                VARCHAR(20)  NOT NULL,
    mail                VARCHAR(120) NOT NULL,
    celular             VARCHAR(30)  NOT NULL,
    contrasena_hash     VARCHAR(255) NOT NULL,
    fecha_alta          DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    cuenta_mercado_pago VARCHAR(60)  NULL,  -- nulo = no vinculó MP (navegación libre)
    CONSTRAINT uq_usuario_mail UNIQUE (mail),
    CONSTRAINT uq_usuario_celular UNIQUE (celular),
    CONSTRAINT uq_usuario_dni UNIQUE (dni)
) ENGINE=InnoDB;

-- =====================================================================
-- 2. PRODUCTO
-- =====================================================================
CREATE TABLE producto (
    id_producto   INT AUTO_INCREMENT PRIMARY KEY,
    nombre        VARCHAR(100) NOT NULL,
    tipo          VARCHAR(50)  NOT NULL,
    precio        DECIMAL(10,2) NOT NULL DEFAULT 0
) ENGINE=InnoDB;

-- =====================================================================
-- 3. HELADERA
-- =====================================================================
CREATE TABLE heladera (
    id_heladera   INT AUTO_INCREMENT PRIMARY KEY,
    ubicacion     VARCHAR(150) NOT NULL,
    estado        VARCHAR(30)  NOT NULL DEFAULT 'Habilitada' -- Habilitada / Mantenimiento / Deshabilitada
) ENGINE=InnoDB;

-- =====================================================================
-- 4. STOCK (entidad asociativa fuerte Producto N:M Heladera)
-- =====================================================================
CREATE TABLE stock (
    id_stock          INT AUTO_INCREMENT PRIMARY KEY,
    id_heladera       INT NOT NULL,
    id_producto       INT NOT NULL,
    lote              VARCHAR(40)  NOT NULL,
    cantidad          INT NOT NULL DEFAULT 0,
    cantidad_limite   INT NOT NULL DEFAULT 0,
    fecha_vencimiento DATE NOT NULL,
    CONSTRAINT fk_stock_heladera FOREIGN KEY (id_heladera) REFERENCES heladera(id_heladera)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_stock_producto FOREIGN KEY (id_producto) REFERENCES producto(id_producto)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT uq_stock_heladera_producto_lote UNIQUE (id_heladera, id_producto, lote)
) ENGINE=InnoDB;

-- =====================================================================
-- 5. COCINA_CENTRAL (relación "Produce" con Producto, N:M -> orden de producción)
-- =====================================================================
CREATE TABLE cocina_central (
    id_produccion       INT AUTO_INCREMENT PRIMARY KEY,
    id_producto         INT NOT NULL,
    id_heladera_destino INT NULL,        -- heladera a la que se destina la producción
    fecha_produccion    DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    cantidad_producto   INT NOT NULL,    -- "Cantidad producido"
    CONSTRAINT fk_cocina_producto FOREIGN KEY (id_producto) REFERENCES producto(id_producto)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_cocina_heladera FOREIGN KEY (id_heladera_destino) REFERENCES heladera(id_heladera)
        ON DELETE SET NULL ON UPDATE CASCADE
) ENGINE=InnoDB;

-- =====================================================================
-- 6. ENCARGADO_DE_MANTENIMIENTO
-- =====================================================================
CREATE TABLE encargado_mantenimiento (
    id_encargado  INT AUTO_INCREMENT PRIMARY KEY,
    nombre        VARCHAR(60) NOT NULL,
    apellido      VARCHAR(60) NOT NULL,
    telefono      VARCHAR(30) NOT NULL
) ENGINE=InnoDB;

-- =====================================================================
-- 7. ACTUALIZACIONES_DE_MANTENIMIENTO (N:M entre Encargado y Heladera)
-- =====================================================================
CREATE TABLE actualizacion_mantenimiento (
    id_actualizacion    INT AUTO_INCREMENT PRIMARY KEY,
    id_encargado        INT NOT NULL,
    id_heladera         INT NOT NULL,
    fecha               DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    observacion         VARCHAR(500) NULL,
    tipo_actualizacion  VARCHAR(60)  NOT NULL, -- Limpieza / Reparación / Revisión / Calibración
    CONSTRAINT fk_actmant_encargado FOREIGN KEY (id_encargado) REFERENCES encargado_mantenimiento(id_encargado)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_actmant_heladera FOREIGN KEY (id_heladera) REFERENCES heladera(id_heladera)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB;

-- =====================================================================
-- 8. ADMINISTRACION_DE_HELADERAS (relación "Gestiona" 1:N con Heladera)
-- =====================================================================
CREATE TABLE administracion_heladera (
    id_administracion INT AUTO_INCREMENT PRIMARY KEY,
    id_heladera        INT NOT NULL,
    fecha              DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    observacion        VARCHAR(500) NULL,
    accion             VARCHAR(100) NOT NULL, -- Reposición / Baja de falla / Modificación de límites, etc.
    CONSTRAINT fk_admheladera_heladera FOREIGN KEY (id_heladera) REFERENCES heladera(id_heladera)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB;

-- =====================================================================
-- 9. CUPONES (relación "Tiene" 1:N con Usuario)
-- =====================================================================
CREATE TABLE cupon (
    id_cupon            INT AUTO_INCREMENT PRIMARY KEY,
    id_usuario          INT NOT NULL,
    codigo              VARCHAR(30)  NOT NULL,
    porcentaje_dto      DECIMAL(5,2) NOT NULL,
    fecha_vencimiento   DATE NOT NULL,
    estado              VARCHAR(20)  NOT NULL DEFAULT 'Disponible', -- Disponible / Usado / Vencido
    CONSTRAINT fk_cupon_usuario FOREIGN KEY (id_usuario) REFERENCES usuario(id_usuario)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT uq_cupon_codigo UNIQUE (codigo)
) ENGINE=InnoDB;

-- =====================================================================
-- 10. COMPRAS
--     "Realiza" -> id_usuario_comprador / "Retira" -> id_usuario_consumidor
--     "Asociada" -> id_heladera ; usa cupón opcionalmente
-- =====================================================================
CREATE TABLE compra (
    id_compra             INT AUTO_INCREMENT PRIMARY KEY,
    id_usuario_comprador  INT NOT NULL,
    id_usuario_consumidor INT NOT NULL,
    id_heladera           INT NOT NULL,
    id_cupon              INT NULL,
    total                 DECIMAL(10,2) NOT NULL DEFAULT 0,
    fecha                 DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    token_apertura        VARCHAR(20) NOT NULL,
    estado                VARCHAR(20) NOT NULL DEFAULT 'Pendiente', -- Pendiente/Retirada/Vencida-Reembolsada
    CONSTRAINT fk_compra_comprador FOREIGN KEY (id_usuario_comprador) REFERENCES usuario(id_usuario)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_compra_consumidor FOREIGN KEY (id_usuario_consumidor) REFERENCES usuario(id_usuario)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_compra_heladera FOREIGN KEY (id_heladera) REFERENCES heladera(id_heladera)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_compra_cupon FOREIGN KEY (id_cupon) REFERENCES cupon(id_cupon)
        ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT uq_compra_token UNIQUE (token_apertura)
) ENGINE=InnoDB;

-- =====================================================================
-- 11. DETALLE_DE_COMPRA (1:N con Compra, 1:N con Producto)
-- =====================================================================
CREATE TABLE detalle_compra (
    id_detalle    INT AUTO_INCREMENT PRIMARY KEY,
    id_compra     INT NOT NULL,
    id_producto   INT NOT NULL,
    cantidad      INT NOT NULL DEFAULT 1,
    subtotal      DECIMAL(10,2) NOT NULL DEFAULT 0,
    CONSTRAINT fk_detalle_compra FOREIGN KEY (id_compra) REFERENCES compra(id_compra)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_detalle_producto FOREIGN KEY (id_producto) REFERENCES producto(id_producto)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB;

-- =====================================================================
-- 12. COMENTARIOS (relación entre Usuario y Producto, ligada a una compra verificada)
-- =====================================================================
CREATE TABLE comentario (
    id_comentario  INT AUTO_INCREMENT PRIMARY KEY,
    id_usuario     INT NOT NULL,
    id_producto    INT NOT NULL,
    id_detalle     INT NULL, -- referencia a la compra verificada que habilita el comentario
    fecha          DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    detalle        VARCHAR(1000) NOT NULL,
    calificacion   TINYINT NOT NULL DEFAULT 5, -- 1 a 5
    CONSTRAINT fk_comentario_usuario FOREIGN KEY (id_usuario) REFERENCES usuario(id_usuario)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_comentario_producto FOREIGN KEY (id_producto) REFERENCES producto(id_producto)
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT fk_comentario_detalle FOREIGN KEY (id_detalle) REFERENCES detalle_compra(id_detalle)
        ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT chk_calificacion CHECK (calificacion BETWEEN 1 AND 5)
) ENGINE=InnoDB;

-- =====================================================================
-- 13. GRUPO (relación N:M entre Usuario y "grupo lógico" -> tabla puente)
--     ID_Grupo identifica al grupo; ID_Usuario a cada integrante
-- =====================================================================
CREATE TABLE grupo (
    id_grupo      INT NOT NULL,
    id_usuario    INT NOT NULL,
    fecha_ingreso DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    es_titular    BOOLEAN NOT NULL DEFAULT FALSE, -- titular = posee cuenta MP asociada al grupo
    PRIMARY KEY (id_grupo, id_usuario),
    CONSTRAINT fk_grupo_usuario FOREIGN KEY (id_usuario) REFERENCES usuario(id_usuario)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB;

-- =====================================================================
-- 14. SALDO (relación "Utiliza" 1:N con Grupo)
-- =====================================================================
CREATE TABLE saldo (
    id_saldo       INT AUTO_INCREMENT PRIMARY KEY,
    id_grupo       INT NOT NULL,
    monto          DECIMAL(10,2) NOT NULL,
    fecha_carga    DATETIME NULL,
    fecha_uso      DATETIME NULL,
    tipo_movimiento VARCHAR(10) NOT NULL DEFAULT 'CARGA', -- CARGA / USO
    CONSTRAINT chk_saldo_tipo CHECK (tipo_movimiento IN ('CARGA','USO'))
) ENGINE=InnoDB;
-- Nota: no se crea FK compuesta a grupo(id_grupo, id_usuario) porque Saldo se asocia
-- al Grupo como entidad colectiva (mismo id_grupo, sin importar el usuario individual).
-- Se valida a nivel aplicación que id_grupo exista en al menos una fila de "grupo".

SET FOREIGN_KEY_CHECKS = 1;

